//获取两个节点间的路径值
export function getPath(nodeId1, nodeId2) {
    var node1 = roadNodedata.find(item => parseInt(nodeId1) === item.id)
    var node2 = roadNodedata.find(item => parseInt(nodeId2) === item.id)
    var x1 = node1.x
    var x2 = node2.x
    var y1 = node1.y
    var y2 = node2.y
    return [x1, y1, x2, y2]
}